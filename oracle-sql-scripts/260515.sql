--입사일이 가장 빠른 60번 부서 사원보다 늦게 입사한 사원의 급여 10% 인상하고 80번 부서로 옮김
SELECT ENAME, MIN(HIREDATE)
FROM EMP_AA
WHERE DEPTNO = 60
GROUP BY ENAME;
SELECT MIN(HIREDATE)
FROM EMP_AA
WHERE DEPTNO = 60;
SELECT HIREDATE
FROM EMP_AA
WHERE HIREDATE > (SELECT HIREDATE FROM EMP_AA WHERE DEPTNO=60);
SELECT *
FROM EMP_AA 
WHERE HIREDATE > (SELECT MIN(HIREDATE)
				  FROM EMP_AA
				  WHERE DEPTNO = 60);
UPDATE EMP_AA 
SET SAL = SAL*1.1,
    DEPTNO = 80
WHERE HIREDATE > (SELECT MIN(HIREDATE)
				  FROM EMP_AA
				  WHERE DEPTNO = 60);
