SELECT EMPNO
FROM EMP_AA E JOIN SALGRADE_AA S ON (E.SAL BETWEEN S.LOSAL AND S.HISAL)
WHERE S.GRADE = 5;
DELETE EMP_AA 
WHERE EMPNO IN (SELECT EMPNO
			    FROM EMP_AA E JOIN SALGRADE_AA S ON (E.SAL BETWEEN S.LOSAL AND S.HISAL)
				WHERE S.GRADE = 5);
