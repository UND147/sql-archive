SELECT
	ENAME AS "사원이름",
	(SAL * 12)AS "연봉"
FROM
	EMP
WHERE
	(SAL * 12) = 36000;
-- 모든 사원의 이름과 연봉을 출력
-- 연봉은 내림차순으로 출력
SELECT
	ENAME AS "사원이름",
	(SAL * 12)AS "연봉"
FROM
	EMP
ORDER BY
	"연봉" DESC;

SELECT
	ENAME AS "사원이름",
	(SAL * 12)AS "연봉"
